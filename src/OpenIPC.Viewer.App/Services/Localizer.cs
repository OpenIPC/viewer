namespace OpenIPC.Viewer.App.Services;

// Tiny ICU-free localizer. Static singleton because XAML's {Binding [Key],
// Source={x:Static svc:Localizer.Instance}} pattern needs a static reference.
// SetLanguage raises PropertyChanged with an empty name ("everything changed")
// so every binding on Localizer.Instance re-reads when the active dict flips —
// no relaunch needed. Note: the WPF "Item[]" indexer convention does NOT work
// here — Avalonia's accessor only re-reads when PropertyName equals the CLR
// indexer name ("Item") or is null/empty, so persistent controls like the
// bottom nav stayed stale on "Item[]". Empty name is the reliable signal.
//
// EN/RU only for now; adding a third language = one more dict + one more
// LangCode value. Missing keys fall back to the key itself, so adding a new
// string in XAML before translating shows up as the key (loud failure mode).
public sealed class Localizer : INotifyPropertyChanged
{
    public static Localizer Instance { get; } = new();

    // _current is left nullable on purpose. Static field init runs in
    // declaration order — Instance (line above) runs before English/Russian
    // are assigned, so any field initializer like `= English` would capture
    // null. By the time anyone reads the indexer, all static initializers
    // for this type have completed, so `?? English` is safe and never null.
    // SetLanguage assigns _current properly once called.
    private IReadOnlyDictionary<string, string>? _current;
    private LangCode _active = LangCode.English;

    public event PropertyChangedEventHandler? PropertyChanged;

    public string this[string key] => (_current ?? English).TryGetValue(key, out var v) ? v : key;
    public LangCode Active => _active;

    public void SetLanguage(LangCode code)
    {
        var resolved = code == LangCode.System ? DetectSystem() : code;
        if (resolved == _active && _current is not null) return;

        _current = resolved switch
        {
            LangCode.Russian => Russian,
            _ => English,
        };
        _active = resolved;
        // Empty/null name = "all properties changed"; Avalonia's INPC accessor
        // re-reads on string.IsNullOrEmpty(PropertyName). "Item[]" would not match.
        PropertyChanged?.Invoke(this, new PropertyChangedEventArgs(string.Empty));
    }

    private static LangCode DetectSystem()
    {
        var name = CultureInfo.CurrentUICulture.TwoLetterISOLanguageName;
        return name.Equals("ru", System.StringComparison.OrdinalIgnoreCase)
            ? LangCode.Russian
            : LangCode.English;
    }

